# 位运算
a = 10
b = 5
print(a & b)
# 0000 1010
# 0000 0101
# 0000 0000   0

b = 6
print(a | b)
# 0000 1010
# 0000 0110
# 0000 1110   14

print(a ^ b)
# 0000 1010
# 0000 0110
# 0000 1100  12
# 异或具有二次还原性
c = a ^ b
print(c ^ a)
print(c ^ b)

print(~b)  # 或者取 -b - 1
# 0000 0110
# 1111 1001  补码
# 1111 1000  反码
# 1000 0111  源码 -7

print(b << 1)
# 0000 0110
# 0000 1100    12

print(b >> 1)
# 0000 0110
# 0000 0011    3

